/**
* add - adds 2 numbers
* @a: first number
* @b: second number
* Return: result
*/
int add(int a, int b)
{
	return (a + b);
}

/**
* sub - subtract one number from another
* @a: first number
* @b: second number
* Return: result
*/
int sub(int a, int b)
{
	return (a - b);
}

/**
* div - divide 2 numbers
* @a: first number
* @b: second number
* Return: result
*/
int div(int a, int b)
{
	return (a / b);
}

/**
* mod - mod of 2 numbers
* @a: first number
* @b: second number
* Return: result
*/
int mod(int a, int b)
{
	return (a % b);
}

/**
* mul - multiplies 2 numbers
* @a: first number
* @b: second number
* Return: result
*/
int mul(int a, int b)
{
	return (a * b);
}
